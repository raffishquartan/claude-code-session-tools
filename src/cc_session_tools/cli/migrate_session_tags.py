"""Migrate .tag files from ~/.claude/projects/**/*.tag to the flat cache dir.

Generated by: src/cc_session_tools/cli/migrate_session_tags.py
Exposed via: ccst tags migrate [--dry-run] [--tags-dir <path>]

Algorithm:
  1. Glob ~/.claude/projects/**/*.tag for source files.
  2. Destination dir is _session_tags_dir() (respects CCCS_SESSION_TAGS_DIR).
  3. For each source file:
     a. dst = dst_dir / src.name  (UUID is already unique; .name == "<uuid>.tag")
     b. If dst exists and content matches: skip (already migrated).
     c. If dst exists and content differs: WARN and skip.
     d. Copy src to dst (shutil.copy2 to preserve mtime).
  4. Verify each copied file: assert dst content == src content.
  5. Print summary + the find command to delete source files.

Source files are NEVER deleted by this command. The user runs the printed
find ... -delete command manually after reviewing the output.
"""
from __future__ import annotations

import argparse
import shutil
import sys
from pathlib import Path

from cc_session_tools.lib.sessions import _session_tags_dir


def _projects_dir() -> Path:
    return Path.home() / ".claude" / "projects"


def run_migration(
    *,
    dry_run: bool,
    dst_dir: Path,
    projects_dir: Path,
) -> int:
    """Execute or simulate the migration. Returns 0 on success, 1 on any error."""
    src_files = list(projects_dir.glob("**/*.tag"))

    if not src_files:
        print(f"No .tag files found under {projects_dir} — nothing to migrate.")
        return 0

    print(f"Found {len(src_files)} .tag file(s) under {projects_dir}")
    if dry_run:
        print("(dry-run mode — no files will be written)")
    print()

    if not dry_run:
        dst_dir.mkdir(parents=True, exist_ok=True)

    copied = 0
    skipped_already = 0
    warned = 0

    for src in sorted(src_files):
        dst = dst_dir / src.name
        try:
            src_content = src.read_text()
        except OSError as exc:
            print(f"  ERROR reading {src}: {exc}", file=sys.stderr)
            warned += 1
            continue

        if dst.exists():
            try:
                dst_content = dst.read_text()
            except OSError as exc:
                print(f"  ERROR reading dst {dst}: {exc}", file=sys.stderr)
                warned += 1
                continue
            if dst_content == src_content:
                print(f"  skip (already present): {src.name}")
                skipped_already += 1
                continue
            else:
                print(
                    f"  WARN: {src.name} exists in dst with different content "
                    f"— skipping (src={src_content.strip()!r}, "
                    f"dst={dst_content.strip()!r})",
                    file=sys.stderr,
                )
                warned += 1
                continue

        if dry_run:
            print(f"  would copy: {src} -> {dst}")
            copied += 1
            continue

        try:
            shutil.copy2(src, dst)
        except OSError as exc:
            print(f"  ERROR copying {src} -> {dst}: {exc}", file=sys.stderr)
            warned += 1
            continue

        # Verify
        try:
            if dst.read_text() != src_content:
                print(
                    f"  ERROR: verify failed for {dst} (content mismatch after copy)",
                    file=sys.stderr,
                )
                warned += 1
                continue
        except OSError as exc:
            print(f"  ERROR verifying {dst}: {exc}", file=sys.stderr)
            warned += 1
            continue

        print(f"  copied: {src.name}  ({src_content.strip()!r})")
        copied += 1

    print()
    if dry_run:
        print(f"Dry-run summary: {copied} would be copied, {skipped_already} already present, {warned} warnings")
    else:
        print(f"Summary: {copied} copied, {skipped_already} already present, {warned} warnings")

    if not dry_run and copied > 0:
        print()
        print("Review the output above. To remove source files once satisfied, run:")
        print(f"  find {projects_dir} -name '*.tag' -delete")

    return 1 if warned > 0 else 0


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(
        description=(
            "Migrate .tag files from ~/.claude/projects/**/*.tag "
            "to the flat cache dir (~/.cache/claude/session-tags/). "
            "Source files are never deleted — run the printed find command manually."
        )
    )
    ap.add_argument(
        "--dry-run",
        action="store_true",
        help="Print what would be copied without writing anything.",
    )
    ap.add_argument(
        "--tags-dir",
        default=None,
        metavar="PATH",
        help=(
            "Override destination directory "
            "(default: from CCCS_SESSION_TAGS_DIR or ~/.cache/claude/session-tags/)"
        ),
    )
    ap.add_argument(
        "--projects-dir",
        default=None,
        metavar="PATH",
        help="Override source projects directory (default: ~/.claude/projects/)",
    )
    args = ap.parse_args(argv)

    dst_dir = Path(args.tags_dir) if args.tags_dir else _session_tags_dir()
    projects_dir = Path(args.projects_dir) if args.projects_dir else _projects_dir()

    print(f"Source : {projects_dir}")
    print(f"Dest   : {dst_dir}")
    print()

    return run_migration(dry_run=args.dry_run, dst_dir=dst_dir, projects_dir=projects_dir)


if __name__ == "__main__":
    sys.exit(main())
