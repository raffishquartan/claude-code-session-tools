"""Rank contributors by token-saved-per-risk and render report artifacts."""
import json

from measure import Contributor
from duplicates import DuplicatePair

HIGH_COST = 1500


def rank(recs: list[Contributor], usage: dict[str, int],
         dup_pairs: list[DuplicatePair]) -> list[dict]:
    redundant = {p.redundant for p in dup_pairs}
    out: list[dict] = []
    for r in recs:
        calls = usage.get(r.name)
        if r.name in redundant:
            tier = "strong"
        elif calls == 0 and r.tokens >= HIGH_COST:
            tier = "strong"
        elif (calls == 0 or calls is None) and r.tokens < HIGH_COST:
            tier = "mention"
        elif r.tokens >= HIGH_COST:
            tier = "trim"
        else:
            tier = "keep"
        out.append({"name": r.name, "category": r.category, "tokens": r.tokens,
                    "usage": calls, "tier": tier, "detail": r.detail})
    out.sort(key=lambda d: ({"strong": 0, "trim": 1, "mention": 2, "keep": 3}[d["tier"]],
                            -d["tokens"]))
    return out


GENERATED_BY = "reduce-persistent-context/analyze_context.py"


def render_json(ranked: list[dict], total: int) -> str:
    return json.dumps(
        {"generated_by": GENERATED_BY, "total_tokens": total,
         "contributors": ranked},
        indent=2)


def render_markdown(ranked: list[dict], total: int) -> str:
    lines = ["# Persistent context report",
             f"<!-- generated by {GENERATED_BY} -->", "",
             f"**Total: {total:,} tokens**", "",
             "| Contributor | Category | Tokens | Usage | Tier |",
             "|---|---|---:|---:|---|"]
    for r in ranked:
        u = "-" if r["usage"] is None else r["usage"]
        lines.append(
            f"| {r['name']} | {r['category']} | {r['tokens']:,} | {u} | {r['tier']} |")
    return "\n".join(lines)
